    private void resetAll(){
        if (txtcodcliente.getText().equals("") || txtcodcliente.getText() == null || txtcodcliente.getText().equals(" ")) {
            txtcodcliente.setText("Ingrese el código del cliente");
        }
        if (txtnomprod.getText().equals("") || txtnomprod.getText() == null || txtnomprod.getText().equals(" ")) {
            txtnomprod.setText("Ingrese el nombre del producto");
        }
        if (txtnomprod.getText().equals("") || txtnomprod.getText() == null || txtnomprod.getText().equals(" ")) {
            txtnomprod.setText("Cantidad");
        }
    }

    private void crearLista(){
        //Este método crea una lista con los nombres de los productos y se la pasamos al método cargar sugerencias.
        //.trim elimina los espacios en blanco del inicio y final
        listaNombres = new JList();
        modeloLista = new DefaultListModel();
        for(String[] i: productos){
            modeloLista.addElement(i[1].trim());
        }
        listaNombres.setModel(modeloLista);
    }

