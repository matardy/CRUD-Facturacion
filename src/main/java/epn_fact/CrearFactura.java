    private void btnAgregarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMousePressed
        //agrego los productos a mi tabla
        String precioUni = methods.searchRowProducto(Integer.parseInt(txtcodprod.getText())).get(0).precioProducto;
        String precioTot = String.valueOf(Float.parseFloat(precioUni) * Integer.parseInt(txtcantidad.getText()));
        String[] row = {txtcantidad.getText(), txtnomprod.getText(), precioUni, precioTot};
        tabla.addRow(row);
        
        //agrego el producto a una lista para crear los detalles
        producto = methods.searchRowProducto(Integer.parseInt(txtcodprod.getText()));
        productosStored.add(producto.get(0));
        
        //calculo subtotal, iva y total
        subTotal += Double.parseDouble(precioTot);
        IVA = subTotal * valorIVA;
        total = subTotal + IVA;
        
        txtSubtotal.setText(String.valueOf(subTotal));
        txtIVA.setText(String.valueOf(IVA));
        txtTotal.setText(String.valueOf(total));
    }//GEN-LAST:event_btnAgregarMousePressed

