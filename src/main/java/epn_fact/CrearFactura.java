                    //String totalFactura,String subtotal, String descuento, String iva,String total)
        String numFactura = methods.setFactura(txtcodcliente.getText(), fecha, "Efectivo", String.valueOf(total),
                String.valueOf(subTotal), "1", String.valueOf(IVA), String.valueOf(total));
