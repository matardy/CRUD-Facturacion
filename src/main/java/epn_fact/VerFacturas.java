    private void btnBuscarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMousePressed
        //Buscar por nombre
        if (!txtNombre.getText().equals("Ingrese el nombre de la persona a buscar")
                && !txtNombre.getText().equals("") && txtNombre.getText() != null) {
            dtmFactura.setRowCount(0);
            String nomAux;
            ArrayList<String[]> facturaFilterClienteView = new ArrayList<>();
            for (String[] i : clientes) {
                nomAux = i[2].trim() + " " + i[3].trim();
                if (txtNombre.getText().equals(nomAux)) {
                    facturaFilterClienteView = methods.facturaFilterClienteView(i[0]);
                }
            }
            for (String[] i : facturaFilterClienteView) {
                dtmFactura.addRow(new Object[]{i[0],i[1],i[2],i[3],i[7],i[6],i[8]});
            }
            return;
        }
        
        //Buscar entre fechas
        if (!txtFechaInicio.getText().equals("Fecha de Inicio") && !txtFechaFin.getText().equals("Fecha de Fin")
                && !txtFechaInicio.getText().equals("") && txtFechaInicio.getText() != null
                && !txtFechaFin.getText().equals("") && txtFechaFin.getText() != null) {
            
            dtmFactura.setRowCount(0);
            ArrayList<String[]> viewFactura = new ArrayList<>();
            viewFactura = methods.facturaFilterFechaView(txtFechaInicio.getText(), txtFechaFin.getText());

            for (String[] i : viewFactura) {
                dtmFactura.addRow(new Object[]{i[0],i[1],i[2],i[3],i[7],i[6],i[8]});
            }
        }
    }//GEN-LAST:event_btnBuscarMousePressed

