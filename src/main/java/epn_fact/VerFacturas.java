    private void getFacturas() throws SQLException{
        dtmFactura.setRowCount(0);
        ArrayList<String[]> viewFacturas = new ArrayList<>();
        viewFacturas = methods.facturaView();
        for(String[] i : viewFacturas){
            dtmFactura.addRow(new Object[]{i[0],i[1],i[2],i[3],i[7],i[6],i[8]});
        }
    }
    
    private void cargarSugerencias(){
        crearLista();
        AutoCompleteDecorator.decorate(listaNombres, txtNombre, ObjectToStringConverter.DEFAULT_IMPLEMENTATION);
    }
    
    private void crearLista(){
        //.trim elimina los espacios en blanco del inicio y final
        listaNombres = new JList();
        modeloLista = new DefaultListModel();
        String nomAux = "";
        for(String[] i: clientes){
            nomAux = i[2].trim() + " " +i[3].trim();
            modeloLista.addElement(nomAux);
        }
        listaNombres.setModel(modeloLista);
    }
    
    private void llenar(){
        //obtengo toda la base de clientes
        clientes = methods.clienteView();
    }
    
    private void resetAll(){
        if (txtNombre.getText().equals("") || txtNombre.getText() == null || txtNombre.getText().equals(" ")) {
            txtNombre.setText("Ingrese el nombre de la persona a buscar");
        }
        if (txtFechaInicio.getText().equals("") || txtFechaInicio.getText() == null || txtFechaInicio.getText().equals(" ")) {
            txtFechaInicio.setText("Fecha de Inicio");
        }
        if (txtFechaFin.getText().equals("") || txtFechaFin.getText() == null || txtFechaFin.getText().equals(" ")) {
            txtFechaFin.setText("Fecha de Fin");
        }
        
    }
    

